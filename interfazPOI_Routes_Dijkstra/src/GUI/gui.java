            int yPOI = poiGetInfo.getCoordY();

            if((10 > (evt.getX() - xPOI) && (evt.getX() - xPOI) > 0) && 
                    (10 > (evt.getY() - yPOI) && (evt.getY() - yPOI) > 0)){ //REVISA SI EL CLICK FUE DENTRO DEL CUADRADO, SE PONE ENTRE 0 Y EL TAMAO DEL CUADRADO
                operationsHub(poiGetInfo);

            }
